first_number = float(input("Digite o numero A: "))
second_number = float(input("Digite o numero B: "))

print(f"{first_number} + {second_number} = {first_number+second_number}")
print(f"{first_number} - {second_number} = {first_number-second_number}")
print(f"{first_number} / {second_number} = {first_number/second_number}")
print(f"{int(first_number)} // {second_number} = {first_number//second_number}")
print(f"{int(first_number)} % {int(second_number)} = {first_number%second_number}")